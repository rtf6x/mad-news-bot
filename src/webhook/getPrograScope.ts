function getLastDigit(num) {
  const toText = num.toString();
  const lastChar = toText.slice(-1);
  return +(lastChar);
}

function getIndex(num) {
  if (num < 0) {
    return 0;
  }
  if (horoscopes[num]) {
    return num;
  }
  return getIndex(num - horoscopes.length);
}

export default function getPrograscope(senderId) {
  const userDigit = getLastDigit(senderId);
  const dateIndex = new Date().getDate();
  const monthIndex = new Date().getMonth();
  const horoscopeIndex = getIndex(userDigit * dateIndex * monthIndex);
  return `Прогноз для программиста #${getLastDigit(senderId)}: ${horoscopes[horoscopeIndex]}`;
}
